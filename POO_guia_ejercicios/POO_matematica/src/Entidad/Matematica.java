
package Entidad;


public class Matematica {
    private double numeroRealUno;
    private double numeroRealDos;

    public Matematica() {
    }

    public Matematica(double numeroRealUno, double numeroRealDos) {
        this.numeroRealUno = numeroRealUno;
        this.numeroRealDos = numeroRealDos;
    }

    public void setNumeroRealUno(double numeroRealUno) {
        this.numeroRealUno = numeroRealUno;
    }

    public void setNumeroRealDos(double numeroRealDos) {
        this.numeroRealDos = numeroRealDos;
    }

    public double getNumeroRealUno() {
        return numeroRealUno;
    }

    public double getNumeroRealDos() {
        return numeroRealDos;
    }
    
    
    
}
