package Entidad;

/**
 *
 * @author cris-
 */
public abstract class AbstractAnimal implements Animal {
    protected String nombre;
    protected String alimento;
    protected int edad;
    protected String raza;

    public AbstractAnimal(String nombre, String alimento, int edad, String raza) {
        this.nombre = nombre;
        this.alimento = alimento;
        this.edad = edad;
        this.raza = raza;
    }
    
    
}
